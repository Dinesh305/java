public class methof_overload1 {

    public void add(int a , int b)
    {
        System.out.println("it is method over loading addition " + (a+ b));

    }
    public void add(int a , int b,int c)
    {
        System.out.println("it is method over loading multiplication " + (a-b-c));

    }
    public void add(int a,int b, int c, int d){
        System.out.println("it is division : " + (a+b+c+d));
    }
}
